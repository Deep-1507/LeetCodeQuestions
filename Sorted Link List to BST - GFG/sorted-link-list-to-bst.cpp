class Solution{
  public:
  
    int getLength(LNode* head){
        int count = 0;
        while(head!=NULL){
            count++;
            head = head->next;
        }
        
        return count;
    }
    
    TNode* util(LNode* &head, int len){
        if(len == 0) return NULL;
        TNode* root = new TNode(0);
        root->left = util(head, len/2);
        root->data = head->data;
        head = head->next;
        root->right = util(head, (len-1)/2);
        return root;
    }
  
    TNode* sortedListToBST(LNode *head) {
        //code here
        int len = getLength(head);
        if(len == 0){
            return nullptr;
        }
        TNode* ans = util(head, len);
        return ans;
    }
};

